package com.kuaileren.common;

import java.util.ArrayList;
import java.util.List;

import com.kuaileren.domain.CommentDO;

public class CommentResult extends BaseResult {

	private CommentDO commentDO =null;
	private List<CommentDO> commentList  = new ArrayList<CommentDO>();
	
	public CommentResult(boolean b) {
		super(b);
	}

	public CommentDO getCommentDO() {
		return commentDO;
	}

	public void setCommentDO(CommentDO commentDO) {
		this.commentDO = commentDO;
	}

	public List<CommentDO> getCommentList() {
		return commentList;
	}

	public void setCommentList(List<CommentDO> commentList) {
		this.commentList = commentList;
	}

	
}
